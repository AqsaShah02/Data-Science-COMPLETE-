# 7. Convert a list into a tuple and vice versa.
# Convert List → Tuple
list1 = [1, 2, 3, 4, 5]
tuple1 = tuple(list1)
print("List:", list1)
print("Converted to Tuple:", tuple1)

# Convert Tuple → List
tuple2 = ('a', 'b', 'c', 'd')
list2 = list(tuple2)
print("Tuple:", tuple2)
print("Converted to List:", list2)
